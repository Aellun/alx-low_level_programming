/**
* _isdigit - function checks if a given character (c) is a digit
* in the range of '0' to '9'
* @c: int to be checked
* Return: 1 if c is a digit, 0 if not
*/
int _isdigit(int c)
{
	return (c >= '0' && c <= '9');/* checks if the character c*/
/*falls within the range of '0' to '9'*/
}
